## Description
# Given a string S, find the length of the longest substring T that contains at most k distinct characters.

## Example
# Input: S = "eceba" and k = 3
# Output: 4
# Explanation: T = "eceb"

# Input: S = "WORLD" and k = 4
# Output: 4
# Explanation: T = "WORL" or "ORLD"

## Challenge
# O(n) time

